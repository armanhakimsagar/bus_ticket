CREATE TABLE IF NOT EXISTS  `fleet_type` (
  `id` INT(11) UNSIGNED NOT NULL AUTO_INCREMENT,
  `type` VARCHAR(255) NOT NULL,
  `status` TINYINT(1) NOT NULL DEFAULT '1',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;




CREATE TABLE IF NOT EXISTS  `fleet_facilities` (
  `id` INT(11) UNSIGNED NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(255) NOT NULL,
  `description` TEXT,
  `status` TINYINT(1) NOT NULL DEFAULT '1',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;



  
CREATE TABLE IF NOT EXISTS `fleet_registration` (
  `id` INT(11) UNSIGNED NOT NULL AUTO_INCREMENT,
  `reg_no` VARCHAR(255) NULL DEFAULT NULL,
  `fleet_type_id` INT(11) NULL DEFAULT NULL,
  `engine_no` VARCHAR(255) NULL DEFAULT NULL,
  `model_no` VARCHAR(255) NULL DEFAULT NULL,
  `chasis_no` VARCHAR(255) NULL DEFAULT NULL,
  `total_seat` INT(11) NULL DEFAULT NULL,
  `seat_numbers` TEXT NULL,
  `fleet_facilities` VARCHAR(255) NULL DEFAULT NULL,
  `owner` VARCHAR(255) NULL DEFAULT NULL,
  `company` VARCHAR(255) NULL DEFAULT NULL,
  `ac_available` TINYINT(1) NULL DEFAULT NULL,
  `status` TINYINT(1) NOT NULL DEFAULT '1',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
